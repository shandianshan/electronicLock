void draw(char* str){
	unsigned char len=strlen(str);
	//the row var is the cell of real row
	unsigned char row=len/16;
	//the col is the number of elements of last row
	unsigned char col=len%16;
	//index show us which part of str is processed
	unsigned char index=0;
	unsigned char i;
	unsigned char j;
	LCDClear();	
	//process the first row rows 
	//it only can show a-z char and space,but it can be expended by adding more if statements
	for(i=0;i<row;i++){
		for(j=0;j<64;j+=8){
			if(str[index]>='a'&&str[index]<='z'){
				ByteDisL(2*i,j,list[str[index++]-'a']);
			}else{
				ByteDisL(2*i,j,byte_space);
				index++;
			}
		}
		
		for( j=0;j<64;j+=8){
			if(str[index]>='a'&&str[index]<='z'){
				ByteDisR(2*i,j,list[str[index++]-'a']);
			}else{
				ByteDisR(2*i,j,byte_space);
				index++;
			}
		}
	}

	//process the last one row
	for(j=0;col!=0&&j<8;j++){
		if(str[index]>='a'&&str[index]<='z'){
			ByteDisL(2*row,j*8,list[str[index++]-'a']);
		}else{
			ByteDisL(2*row,8*j,byte_space);
			index++;
		}
		col--;
	}
	for(j=0;col!=0&&j<8;j++){
		if(str[index]>='a'&&str[index]<='z'){
			ByteDisR(2*row,8*j,list[str[index++]-'a']);
		}else{
			ByteDisR(2*row,8*j,byte_space);
			index++;
		}
		col--;
	}
}
void display_star(unsigned char n){
	unsigned char i,j;
	unsigned char row=n/16;
	unsigned char col=n%16;

	LCDClear();
	for(i=0;i<row;i++){
		for(j=0;j<64;j+=8){
			ByteDisL(2*i,j,byte_star);
		}
		
		for(j=0;j<64;j+=8){
			ByteDisR(2*i,j,byte_star);
		}
	}

	//process the last one row
	for(j=0;col!=0&&j<8;j++){
		ByteDisL(2*row,j*8,byte_star);
		col--;
	}
	for(j=0;col!=0&&j<8;j++){
		ByteDisR(2*row,8*j,byte_star);
		col--;
	}
}
